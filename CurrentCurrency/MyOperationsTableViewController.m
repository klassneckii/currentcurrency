#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    // Return the number of rows in the section.
    return self.operations.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:cellIdentifier forIndexPath:indexPath];
    
    MyCurrencyRealm *operation = self.operations[indexPath.row];
    
    NSString *baseStr = [NSString stringWithFormat:@"%0.1f единиц %@ <-> %@", operation.amount, operation.rate.sourceCurrency, operation.rate.destinationCurrency];
    cell.textLabel.text = baseStr;
    
    NSString *baseDetStr = [NSString stringWithFormat:@"Купил %0.2f Продам за %0.2f", operation.rate.buyRate, operation.rate.sellRate];

    cell.detailTextLabel.text = baseDetStr;
    
    // Configure the cell...
    
    return cell;
}


